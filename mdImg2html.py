# 마크다운 형식의 이미지를 HTML <img> 태그로 변환하는 함수 (개선된 버전)
def markdown_to_img(markdown_text):
    # 마크다운 형식에서 URL과 alt text, 스타일 등을 추출
    import re
    pattern = r'!\[(.*?)\]\((.*?)\)'
    match = re.findall(pattern, markdown_text)
    
    img_tags = []
    
    for alt_text, url in match:
        # img 태그 생성
        img_tag = f'<img src="{url}" alt="{alt_text}" />'
        img_tags.append(img_tag)
    
    # 변환된 HTML <img> 태그들을 출력
    return "\n".join(img_tags)

